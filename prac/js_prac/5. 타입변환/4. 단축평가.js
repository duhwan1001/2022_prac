
// OR 연산
console.log('hello' || 'world'); // 둘다 true라 왼쪽거 출력
console.log(0 || '안녕'); // false || true 라서 우항 출력

// AND 연산자
console.log('HELLO' && 'WORLD'); // 우항에 따라서 출력되기때문에 우항 출력
console.log(null && '메롱'); // 좌항이 false기 때문에 좌항 출력

